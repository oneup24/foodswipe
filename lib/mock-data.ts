export function getFilteredRestaurants(
  restaurants: Restaurant[],
  filters: {
    cuisines: string[];
    priceRange: number[];
    maxDistance: number;
    minRating: number;
    openNow: boolean;
  }
): Restaurant[] {
  return restaurants.filter((r) => {
    if (filters.cuisines.length > 0 && !r.cuisine.some((c) => filters.cuisines.includes(c))) {
      return false;
    }
    if (filters.priceRange.length > 0 && !filters.priceRange.includes(r.priceLevel)) {
      return false;
    }
    if (r.distance > filters.maxDistance) return false;
    if (r.rating < filters.minRating) return false;
    if (filters.openNow && !r.isOpen) return false;
    return true;
  });
}
